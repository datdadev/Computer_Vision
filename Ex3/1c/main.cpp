    Mat eroded, temp;

    Mat element = getStructuringElement(MORPH_CROSS, Size(3, 3));
    bool done;

    do {
        erode(img, eroded, element);
        dilate(eroded, temp, element);
        subtract(img, temp, temp);
        bitwise_or(skel, temp, skel);
        eroded.copyTo(img);
        done = (countNonZero(img) == 0);
    } while (!done);

    imshow("Skeleton", skel);
    cout << "Matrix after skeletonization:" << endl << skel << endl;

    Mat output;
    resize(skel * 255, output, Size(), 32, 32, INTER_NEAREST);
    imshow("Skeletonization", output);

    waitKey(0);
    return 0;
