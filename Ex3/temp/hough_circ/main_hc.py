import cv2
import numpy as np

def houghcircle(matrix, min_radius, max_radius):
    img = np.array(matrix, dtype=np.uint8) * 255

    img_blurred = cv2.GaussianBlur(img, (3, 3), 0)

    circles = cv2.HoughCircles(
        img_blurred, 
        cv2.HOUGH_GRADIENT, 
        dp=1, 
        minDist=1, 
        param1=1, 
        param2=1, 
        minRadius=min_radius, 
        maxRadius=max_radius
    )

    if circles is not None:
        circles = np.uint16(np.around(circles))
        print(f"Detected circles with radius of {min_radius}:")
        for i in circles[0, :]:
            print(f"Center: ({i[0]}, {i[1]}); Radius: {i[2]}")
    else:
        print(f"No circles detected with radius of {min_radius}.\n")

